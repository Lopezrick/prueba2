    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       if(txtUsuario.getText().isEmpty()){
           JOptionPane.showMessageDialog(null,"Debe ingresar un nombre de usuario");  
         }  
        
       else if(pasword.getText().equals(password2.getText()) && !txtUsuario.getText().isEmpty()){
           a.agregarUsuario(txtUsuario.getText(),pasword.getText());
           FrmLogin obj=new FrmLogin();
           obj.setVisible(true);
           this.dispose();
           JOptionPane.showMessageDialog(null,"Registro exitoso");
        }
         else{
             JOptionPane.showMessageDialog(null,"Las contrase単as no coinciden");
             pasword.setText("");
             password2.setText("");
             pasword.requestFocus();
         }
         
         
    }//GEN-LAST:event_jButton1ActionPerformed

