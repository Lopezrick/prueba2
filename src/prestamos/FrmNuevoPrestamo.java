    private void cmbMesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbMesActionPerformed
        // TODO add your handling code here:
        int i = cmbMes.getSelectedIndex();
        mdlDia.removeAllElements();
        mdlDia.addElement("DIAS");
        if(i==0){
            cmbDia.setEnabled(false);
        }
        else{
            cmbDia.setEnabled(true);
        }
        
        if(i==1||i==3||i==5||i==7||i==8||i==10||i==12){
           
           for(int j=1;j<=31;j++){
               mdlDia.addElement(""+j);
           }
            
        }
        else if(i==2){
            if(cmbAnio.getSelectedIndex()==0){
              for(int j=1;j<=28;j++){
               mdlDia.addElement(""+j);
              }
            }
            else{
                int a= Integer.parseInt((String) cmbAnio.getSelectedItem());
                
            if(Bisiesto(a)==true){
             for(int j=1;j<=29;j++){
               mdlDia.addElement(""+j);
           }    
            }else{
             for(int j=1;j<=28;j++){
               mdlDia.addElement(""+j);
           }   
            }    
            }
            
            
             
        }
        else{
             for(int j=1;j<=30;j++){
               mdlDia.addElement(""+j);
           }
        }
        cmbDia.setModel(mdlDia);
        
    }//GEN-LAST:event_cmbMesActionPerformed

